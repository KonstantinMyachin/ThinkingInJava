package ru.myachin.exceptions;

public class Switch {

    private boolean state;

    public boolean read() {
        return state;
    }

    public void on() {
        state = true;
        System.out.println(this);
    }

    public void off() {
        state = false;
        System.out.println(this);
    }

    @Override
    public String toString() {
        return state ? "вкл" : "выкл";
    }
}